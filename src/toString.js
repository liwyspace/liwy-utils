/** Used as references for various `Number` constants. **/
const DEF = '-LIWY';

/**
 * Converts `value` to a string.
 *
 * @since 1.0.0
 * @param {*} value the value to convert.
 * @return {String} Returns the converted string.
 * @example
 *
 */
function toString(value) {
    return value + DEF;
}

export default toString;
